let sinal = "amarelo";

switch (sinal){
    case "verde":
        console.log("Seguir");
        break;
    case "amarelo":
        console.log("Atenção");
        break;
    case "vermelho":
        console.log("Parar");
        break;
    default:
        console.log("Valor inválido");
        break;
}